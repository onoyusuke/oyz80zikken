; SAMPLE - 1
;
 ORG 8000H
#PRTHL: EQU 1FBEH
#PRTHX: EQU 1FC1H
#PRNTS: EQU 1FF1H
#PRINT: EQU 1FF4H

 LD HL,8000H
 CALL LNDMP
 RET

LNDMP:
 PUSH HL
 CALL #PRTHL
 POP HL
 LD B,8 ; BITES
 LD E,0 ; CLR SUM
LNDMP1: CALL #PRNTS
 LD A,(HL)
 INC HL
 LD D,A ; SAVE A
 CALL #PRTHX
 ;
 LD A,D ; LOAD A
 ADD A,E
 LD E,A ; LINE SUM
 ;
 DJNZ LNDMP1
 CALL #PRNTS
 LD A,':'
 CALL #PRINT
 LD A,E
 CALL #PRTHX
 RET
